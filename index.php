                    The ceramics are like the bullets, 72 in total, stacked around a circle, on four levels. The water enters the Eightball using one high-pressure anti-chamber, enabling it to maintain a uniform water array flow, and two sub chambers with low pressure, allowing it drain out naturally through gravity and the syphon effect. <br>Water flow is manually variable, as is the water level inside the second chamber, allowing the user to keep none, one third, or two thirds of the ceramics submerged, depending on how advanced is the water cycle. The whole system gives the option of being detachable from the sphere, this allows for the usage of an already established external tank for faster cycling.<br> The exit water drop is at 10 cm height from the surface. The flow will form bubbles at the surface, naturally oxygenating the water. The exit location drop is variable, centered by default.<br> The compact shape and centered weight is optimized for a rapid bacterial colonization, enhanced by the proximity of the walls and special geometric configuration of the biological elements. The cascading water motion helps breaking down the nitrogen cycle with ease. The setup is simple and requires very low maintenance. The whole filtering and oxygenating operation is done in almost complete silence.

